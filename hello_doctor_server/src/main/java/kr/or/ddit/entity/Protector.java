package kr.or.ddit.entity;


public class Protector {

  private String ppNo;
  private String pNo;
  private String ppName;
  private String ppSex;
  private String ppRnum;
  private String ppTel;
  private String ppAddress;


  public String getPpNo() {
    return ppNo;
  }

  public void setPpNo(String ppNo) {
    this.ppNo = ppNo;
  }


  public String getPNo() {
    return pNo;
  }

  public void setPNo(String pNo) {
    this.pNo = pNo;
  }


  public String getPpName() {
    return ppName;
  }

  public void setPpName(String ppName) {
    this.ppName = ppName;
  }


  public String getPpSex() {
    return ppSex;
  }

  public void setPpSex(String ppSex) {
    this.ppSex = ppSex;
  }


  public String getPpRnum() {
    return ppRnum;
  }

  public void setPpRnum(String ppRnum) {
    this.ppRnum = ppRnum;
  }


  public String getPpTel() {
    return ppTel;
  }

  public void setPpTel(String ppTel) {
    this.ppTel = ppTel;
  }


  public String getPpAddress() {
    return ppAddress;
  }

  public void setPpAddress(String ppAddress) {
    this.ppAddress = ppAddress;
  }

}
