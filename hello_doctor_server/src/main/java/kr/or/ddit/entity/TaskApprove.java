package kr.or.ddit.entity;


public class TaskApprove {

  private String tnNo;
  private String taNo;
  private String tnNoti;
  private String tnStatus;
  private String tnYn;


  public String getTnNo() {
    return tnNo;
  }

  public void setTnNo(String tnNo) {
    this.tnNo = tnNo;
  }


  public String getTaNo() {
    return taNo;
  }

  public void setTaNo(String taNo) {
    this.taNo = taNo;
  }


  public String getTnNoti() {
    return tnNoti;
  }

  public void setTnNoti(String tnNoti) {
    this.tnNoti = tnNoti;
  }


  public String getTnStatus() {
    return tnStatus;
  }

  public void setTnStatus(String tnStatus) {
    this.tnStatus = tnStatus;
  }


  public String getTnYn() {
    return tnYn;
  }

  public void setTnYn(String tnYn) {
    this.tnYn = tnYn;
  }

}
