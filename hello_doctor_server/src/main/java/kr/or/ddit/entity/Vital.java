package kr.or.ddit.entity;


public class Vital {

  private String vNo;
  private String mNo;
  private String vTemperature;
  private String vBreath;
  private String vPulse;
  private String maxPressure;
  private String minPressure;
  private String vHeight;
  private String vWeight;


  public String getVNo() {
    return vNo;
  }

  public void setVNo(String vNo) {
    this.vNo = vNo;
  }


  public String getMNo() {
    return mNo;
  }

  public void setMNo(String mNo) {
    this.mNo = mNo;
  }


  public String getVTemperature() {
    return vTemperature;
  }

  public void setVTemperature(String vTemperature) {
    this.vTemperature = vTemperature;
  }


  public String getVBreath() {
    return vBreath;
  }

  public void setVBreath(String vBreath) {
    this.vBreath = vBreath;
  }


  public String getVPulse() {
    return vPulse;
  }

  public void setVPulse(String vPulse) {
    this.vPulse = vPulse;
  }


  public String getMaxPressure() {
    return maxPressure;
  }

  public void setMaxPressure(String maxPressure) {
    this.maxPressure = maxPressure;
  }


  public String getMinPressure() {
    return minPressure;
  }

  public void setMinPressure(String minPressure) {
    this.minPressure = minPressure;
  }


  public String getVHeight() {
    return vHeight;
  }

  public void setVHeight(String vHeight) {
    this.vHeight = vHeight;
  }


  public String getVWeight() {
    return vWeight;
  }

  public void setVWeight(String vWeight) {
    this.vWeight = vWeight;
  }

}
